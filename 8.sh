#!/bin/bash

day=$(date +%a)

case $day in
	Sun)
		echo Sunday, fuck sundays
		;;
	Mon)
		echo Monday, starting to get the weekly rythem
		;;
	Tue)
		echo Tuesday, halfway there
		;;
	Wed)
		echo Wednesday, can you taste the weekend?
		;;
	Thu)
		echo Thursday, weekend is within reach
		;;
	Fri)
		echo Friday, this is the day
		;;
	Sun)
		echo Sunday, cuz every good thing must end
		;;

esac
